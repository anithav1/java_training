public class Assign
{
    public static void main(String[] args) {
        // Given values for 'a' and 'b'
        double a = 20;
        double b = 10;

        // Calculate the result of a square - b square
        double result = Math.pow(a, 2) - Math.pow(b, 2);

        // Output the result
        System.out.println("Result of (a^2 - b^2) where a = 20 and b = 10: " + result);
    }
}
